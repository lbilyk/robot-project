int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART2_UART_Init();
  MX_SPI3_Init();
  MX_I2C1_Init();
  MX_ADC1_Init();
  MX_CAN_Init();
  MX_TIM1_Init();
  MX_DAC1_Init();
  MX_USART1_UART_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();

  //variables
  uint8_t echobit [2];
  int index = 0;

  HAL_UART_Transmit(&huart1, TxData, sizeof(TxData), 1000);


  //HAL_UART_Receive_DMA(&huart2,Rxdata,1);
  while(1) {
      rxbyte = USART_Read(USART1);

      //if the received bit is a delete key
      if (rxbyte == 127) {
          //toggles a light for debug purposes
          HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_5);

          //echo back space to show removal
          HAL_UART_Transmit(&huart1, empty, 2, 1000);

          //set the character that was wrong to null
          Buffer[index - 1] = '\0';

          //index backwards
          index = index - 1;


          //resend the value after deleting
          HAL_UART_Transmit(&huart1, Buffer, sizeof(Buffer), 1000);
      }

      //if the recieved key is enter
      if (rxbyte == '\r' || index == 100) {
          //toggles a light for debug purposes
          //HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_5);
          //adds a new line and end of string to what was sent
          HAL_UART_Transmit(&huart1, "\r\n", 2, 1000);

          //in case you want to see what was sent
          HAL_UART_Transmit(&huart1, Buffer, sizeof(Buffer), 1000);
          HAL_UART_Transmit(&huart1, "\r\n", 2, 1000);

          index = 0;

          //reset buffer
          memset(Buffer, 0, sizeof(Buffer));

      }

      //if recived key is not special case
      if (rxbyte != 127 || rxbyte != '\r') {
          //echo back if no conditions met
          Buffer[index] = rxbyte;
          echobit[0] = Buffer[index];
          HAL_UART_Transmit(&huart1, echobit, 2, 1000);//setting the buffer for whole word and what is echoed back
          index++;
      }
  }

}

